    override fun execute(): Boolean {
        checkClosed()
        try {
            return executeInner(session.execute(bindParameters()), returnNullStrings)
        } catch (t: Throwable) {
            throw SQLException(t.message, t)
        }
    }

    private fun bindParameters(): BoundStatement {
        try {
            return preparedStatement.bind(*params.toTypedArray())
        } finally {
            clearParams()
        }
    }

    private fun clearParams() {
        params.clear()
    }

    @Throws(SQLException::class)
    override fun addBatch() {
        try {
            if (batchStatementBuilder == null) {
                batchStatementBuilder = BatchStatement.builder(BatchType.LOGGED)
            }
            batchStatementBuilder!!.addStatement(preparedStatement.bind(*params.toTypedArray()))
        } catch (t: Throwable) {
            throw SQLException(t.message, t)
        } finally {
            clearParameters()
        }
    }

