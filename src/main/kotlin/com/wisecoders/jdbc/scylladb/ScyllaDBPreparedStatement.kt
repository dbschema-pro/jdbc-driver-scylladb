    @Throws(SQLException::class)
    override fun executeUpdate(): Int {
        checkClosed()
        try {
            result = ScyllaDBResultSet(this, session.execute(bindParameters()), returnNullStrings)
            if (result!!.isQuery) {
                throw SQLException("Not an update statement")
            }
            return 1
        } catch (ex: SyntaxError) {
            throw SQLSyntaxErrorException(ex.message, ex)
        } catch (t: Throwable) {
            throw SQLException(t.localizedMessage, t)
        }
    }

    @Throws(SQLException::class)
    override fun executeUpdate(sql: String): Int {
        throw SQLException("Method should not be called on prepared statement")
    }

    override fun close() {
        super.close()
        if (result != null) {
            result!!.close()
            result = null
        }
    }

